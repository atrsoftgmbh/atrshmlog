/** \file t_get_inittime_tsc_after.c
 * \brief We are a test program.
 */

#include "../atrshmlog.h"

#include "../atrshmlog_internal.h"

/*
 * ------------------------------------------------------------
 */

#include <stdio.h>


#define PN(_x) printf( #_x " : %ld :\n", (long) _x)

#define PS(_x) printf( #_x " : %s :\n", _x);

#define PP(_x) printf( #_x " : %p :\n", _x);

int main (int argc, char *argv[])
{

  printf("%s\n", argv[0]);

  if (argc != 2)
    exit(1);
  
  // we start without slaves ...
  atrshmlog_set_f_list_buffer_slave_count(0);
  
  atrshmlog_ret_t ret = atrshmlog_attach();

  if (ret != 0)
    {
      printf("%d\n", ret);
      exit(1);
    }

  
  PN(atrshmlog_inittimetsc_after);

  PN(atrshmlog_get_inittime_tsc_after());

  atrshmlog_inittimetsc_after = strtol(argv[1], NULL, 10);

  PN(atrshmlog_inittimetsc_after);

  PN(atrshmlog_get_inittime_tsc_after());
  
  return 0;
}

